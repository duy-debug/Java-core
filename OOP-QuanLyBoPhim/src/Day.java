public class Day {
  private int day, thang, nam;

  public Day(int day, int thang, int nam) {
    this.day = day;
    this.thang = thang;
    this.nam = nam;
  }

  public int getDay() {
    return day;
  }

  public void setDay(int day) {
    this.day = day;
  }

  public int getThang() {
    return thang;
  }

  public void setThang(int thang) {
    this.thang = thang;
  }

  public int getNam() {
    return nam;
  }

  public void setNam(int nam) {
    this.nam = nam;
  }
}
